export const REGEX = {
  email:
    /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  otp: /^[0-9]{1,6}$/,
  password:
    /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[!@#$%^&*()_\-+={}[\]|\\:;"'<>,.?/~`])(?!.*\s).{8,}$/,
  digit: /[^0-9]/g,
  decimal: /^\d*\.?\d+$/,
  letter: /[^a-zA-Z ]/,
  spaceLetter: /[^a-zA-Z ]/,
  name: /^[A-Za-z ]*$/,
  firstName: /^[A-Za-z ]+$/,
  lastName: /^[A-Za-z .]+$/,
  address: /^[0-9A-Za-z\s.,'-]{10,}$/,
  phoneNumber: /^[\+]?[(]?[0-9]{3}[)]?[-\s\.]?[0-9]{3}[-\s\.]?[0-9]{4,6}$/,
  timeRegex: /^(1[0-2]|0?[1-9])\s?[APMapm]{2}$/,
  number: /^\d*$/,
  alphanumeric:
    /^(?=.*[A-Za-z])[a-zA-Z0-9 !@#$%^&*()_+\-=\[\]{};':"\\|,.<>\/?]*$/,
};
